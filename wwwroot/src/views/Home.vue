Tập thể thao đẩy lùi bệnh tật cũng khó

Bauer Hockey – nhà sản xuất gậy khúc côn cầu cao cấp cho các cầu thủ NHL – đang gặp rắc rối khi nhà máy sản xuất của hãng này ở tỉnh Chiết Giang (Trung Quốc) dừng hoạt động từ tháng trước và liên tục trì hoãn mở cửa trở lại.

Khúc côn cầu đòi hỏi người chơi thay gậy liên tục nên vấn đề dần trở nên nghiêm trọng, khiến các giải đấu, sự kiện thể thao bị hoãn vô thời hạn.

Đình trệ cả giao thương và giao tiếp

Nhiều hội nghị thương mại lớn trên thế giới cũng tạm dừng hoặc hủy bỏ, trong đó có Triển lãm Di động toàn cầu – triển lãm điện thoại thông minh lớn nhất thế giới. Sự kiện dự kiến được tổ chức cuối tháng 2 tại Barcelona và thu hút hơn 100.000 người tham dự, hiện đã hủy bỏ hoàn toàn.

Những sự kiện tương tự do các thương hiệu như Swatch, Cisco tổ chức cũng chịu chung số phận. Liên hoan phim Berlin đã không còn trọn vẹn khi hơn 50 đại biểu và nhà quản lý từ Trung Quốc không xin dược thị thực du lịch.

Ngay cả Triển lãm Hàng không Singapore được lên lịch trong vài ngày nữa cũng đối diện thất bại khi hơn 70 đơn vị rút đăng ký. Nhiều thỏa thuận tiềm năng trị giá hàng triệu USD đành gác lại cho tới khi tình hình khả quan hơn.

Giao tiếp giữa người với người cũng đang được hạn chế ở mức tối thiểu. Nhiều sự kiện cộng đồng, sinh hoạt ngày thường đã phải tạm dừng trước lo ngại về bệnh dịch. Có thể nói, dịch viêm phổi do virus Covid-19 có tác động sâu rộng với phạm vi toàn cầu.


