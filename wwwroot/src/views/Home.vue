“Công xưởng của thế giới” Trung Quốc chao đảo trước dịch bệnh do virus corona chủng mới (Covid-19) bùng phát, kéo theo thị trường tiêu dùng toàn cầu.
Dịch bệnh do virus Covid-19 bắt nguồn tại Vũ Hán (Hồ Bắc, Trung Quốc) không chỉ gây ảnh hưởng tới sức khỏe cộng đồng mà còn phủ bóng đen lên nhiều hoạt động giao thương, sản xuất tại đây.

Trước tốc độ dịch bệnh lây lan tại Trung Quốc và hơn 20 quốc gia khác, nhiều nhà máy, xí nghiệp, cơ sở sản xuất buộc phải tạm ngừng hoạt động, gây thiệt hại đáng kể tới nhiều ngành công nghiệp.

Tâm lý mua để tích trữ

Không chỉ Covid-19 mà cả những tin đồn gây hoang mang dư luận cũng góp phần vào tình trạng khan hiếm hàng tiêu dùng. Đặc khu Hồng Kông, nơi có nguồn cung hàng hóa phụ thuộc vào Trung Quốc Đại lục, minh chứng cho tình hình này.

Tin đồn thiếu giấy vệ sinh hồi đầu tháng làm người dân vội quét sạch các kệ siêu thị. Điều tương tự cũng xảy ra với các mặt hàng thiết yếu như gạo, mỳ Ý khi sự hoảng loạn đè nặng tâm lý người mua.

Tin đồn vô căn cứ nhanh chóng trở thành sự thật do mọi người thi nhau tích trữ trong khi các nhà máy sản xuất chưa hoạt động trở lại. Chính quyền Hồng Kông công khai chỉ trích những hành vi này có "mục đích xấu xa" nhằm lan truyền tin giả, gây hoang mang và hỗn loạn.


Thiếu hụt thực phẩm

Dịch bệnh lần này cũng gây đình trệ trong khâu xuất nhập khẩu của Trung Quốc. Nước này chật vật nhập khẩu thịt lợn trong khi nguồn cung nội địa vẫn đang thiếu trầm trọng sau dịch tả lợn châu Phi bùng phát hồi năm ngoái, khiến nước này thiệt hại đến 1/3 đàn lợn.

Trung Quốc hiện đang nhập mặt hàng này từ các nước Châu Âu, Hoa Kỳ hay Brazil. Tuy nhiên, thịt lợn được vận chuyển trong các container đông lạnh phải trải qua nhiều quy trình phức tạp và cần được xử lý cẩn thận.

Ngành may mặc đình trệ

Hãng thể thao UnderArmour cho biết gián đoạn trong sản xuất do dịch bệnh đang gây thiếu hụt vải, bao bì và nguyên liệu thô. Hãng này cũng dự đoán doanh thu quý đầu năm 2020 có thể giảm 60 triệu USD.

Khoản lỗ trên không thể sánh bằng thiệt hại dự tính của nhà mốt tại London - Xuzhi Chen. Sản phẩm của thương hiệu này được may tại Thượng Hải (Trung Quốc) và hãng vẫn không rõ thời điểm kinh doanh trở lại.

Tình hình nói trên đang trở nên phổ biến trong ngành dệt may do nhiều hãng thời trang nước ngoài đặt dây chuyền sản xuất tại Trung Quốc. Sức mua đáng kể từ người tiêu dùng Trung Quốc giảm cũng là một nhân tố quan trọng gây thiệt hại tới ngành may mặc trên thế giới.

Đến sản phẩm "người lớn" cũng trong tầm ảnh hưởng

Tưởng như không liên quan nhưng các mặt hàng "nhạy cảm" như đồ chơi tình dục, bao cao su,... cũng chịu tác động của đại dịch này.

Theo RT, nhiều người lầm tưởng tình trạng khan hiếm bao cao su tại Singapore và Hồng Kông bắt nguồn từ việc người dân dành nhiều thời gian ở nhà cùng bạn đời. Nhưng nhiều bức ảnh trên mạng xã hội cho thấy bao cao su hiện đang là phương án thay thế hiệu quả cho găng tay y tế. Ước tính 1/4 số bao cao su trên toàn cầu được sản xuất tại Trung Quốc.

Chủ một công ty của Nga chuyên bán đồ chơi tình dục cũng cho biết Covid-19 làm doanh nghiệp của ông điêu đứng vì phần lớn sản phẩm hay nguyên liệu sản xuất có nguồn gốc từ Trung Quốc.

